public abstract class Weapon {
    int attackMod;
    int weight;

    public int getAttackMod() {
        return this.attackMod;
    }

    public void setAttackMod(int attackMod) {
        this.attackMod = attackMod;
    }

    public int getWeight() {
        return this.weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

}
