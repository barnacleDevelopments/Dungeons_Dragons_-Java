public class PlayerSelections {
    String playerName;
    String playerType;
    String playerWeaponType;
    int playerAgility;
    int playerDefense;
    int playerAttack;
    int playerHitPoints;

    public String getPlayerType() {
        return this.playerType;
    }

    public void setPlayerType(String playerType) {
        this.playerType = playerType;
    }

    public int getPlayerAgility() {
        return this.playerAgility;
    }

    public void setPlayerAgility(int playerAgility) {
        this.playerAgility = playerAgility;
    }

    public int getPlayerDefense() {
        return this.playerDefense;
    }

    public void setPlayerDefense(int playerDefense) {
        this.playerDefense = playerDefense;
    }

    public int getPlayerAttack() {
        return this.playerAttack;
    }

    public void setPlayerAttack(int playerAttack) {
        this.playerAttack = playerAttack;
    }

    public int getPlayerHitPoints() {
        return this.playerHitPoints;
    }

    public void setPlayerHitPoints(int playerHitPoints) {
        this.playerHitPoints = playerHitPoints;
    }

    public String getPlayerName() {
        return this.playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public String getPlayerWeaponType() {
        return this.playerWeaponType;
    }

    public void setPlayerWeaponType(String playerWeaponType) {
        this.playerWeaponType = playerWeaponType;
    }

}
